<h1>PENICILLIUM ROQUEFORTI </h1>
	
<ul>
  <li>Kingdom: Bacteria </li>
  <li> Phylum: Cyanobacteria</li>
  <li>Order: Chroococcales </li>
  <li>Genus: Synechocystis </li>
</ul>

<h3>Definition and characterisation</h3>

<p>Penicillium roqueforti is a ubiquitous, saprophytic, filamentous fungus that is naturally found in soil and decomposing organic material. Furthermore, because of its high tolerance to different conditions (e.g. alkaline and acid preservatives and low oxygen concentrations and temperatures), this strain is also a spoilage agent that may be found in refrigerated foods, meat and wheat products (Ropars et al. 2012).</p>
<p>This strain’s genome has become recently available, although not annotated, and a number of genes, especially ones that encode for relevant enzymes and secondary metabolites (mycotoxins), have been characterised (NCBI n.d.). </p>
<p>Even though Penicillium roqueforti produces mycotoxins such as isofumigaclavin C, penicillic acid, patulin, PR toxin, roquefortine and botryodiploidin, they do not pose a risk to human, animal or environmental health under normal production conditions (e.g. they are produced when media has a high carbon to nitrogen ratio). Therefore, this strain is considered a non-pathogenic organism with limited cases of infection (Class 1 Containment Agent) (US EPA 1997).</p>

<h3>Growth conditions </h3>

<p>Penicillium roqueforti is an aerobic organism that is able to use different carbohydrate and nitrogen sources to grow over a pH range of 3-8. Its reproduction is asexual (although there has been indirect evidence that this organism has sexual cycles (Ropars et al. 2012)) and conidia production is involved (US EPA 1997).</p>
<p>One of the most used mediums to cultivate this strain is Potato Dextrose Agar (PDA) medium at a range of temperature between 23-25ºC (Gillot et al. 2015). Another medium that can be used is the Malt Extract Peptone agar (DSMZ n.d.).  </p>

<h3>Uses and applications</h3>


<p>This fungal strain has a relevant economic importance, having been its major use the production of blue cheeses like the Roquefort, Stilton, Italian Gorgonzola and Danablu (Gillot et al. 2015) since approximately 500 AD (US EPA 1997).</p>
<p>Furthermore, Penicillium roqueforti is able to produce flavouring agents, polysaccharides, antibacterials, proteases and other industrially relevant enzymes. Similar Penicillium strains can also produce antibiotics, other flavours, fragrances, 2-heptanone, methyl ketones and other specialty chemicals through fermentation processes. It is even possible to use Penicillium roqueforti on bioremediation processes (US EPA 1997).</p>
<p>It is worth mentioning that the production of mycotoxins occurs at higher levels during cheese production compared with the fermentation process for specialty chemicals’ production (US EPA 1997). </p>
