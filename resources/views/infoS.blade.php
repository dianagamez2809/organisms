<h1>SYNECHOCYSTIS SP PCC 6803</h1>
	
<ul>
  <li>Kingdom: Bacteria </li>
  <li> Phylum: Cyanobacteria</li>
  <li>Order: Chroococcales </li>
  <li>Genus: Synechocystis </li>
</ul>

<h3>Definition and characterisation</h3>

<p>Synechocystis sp. PCC 6803 is a unicellular, Gram negative, non-nitrogen-fixing, freshwater cyanobacterium (Gao et al. 2015). It is a well-characterised organism, with its biochemical, physiological and genomic data readily available, since its entire genome has been sequenced and approximately 3,168 genes have been annotated (Kaneko & Tabata 1997). 
</p>
<p>This species is considered a photosynthetic model organism, being able to study on this cyanobacterium the molecular aspects of stress responses and adaptation in plants, because there are biochemical similarities between Synechocystis sp. PCC 6803 and plant chloroplasts (Yu et al. 2013). 
</p>
<p>Moreover, Synechocystis sp. PCC 6803 can be naturally transformed by exogenous DNA (homologous recombination at high frequency) and can grow heterotrophically with glucose as the carbon source (Ikeuchi & Tabata 2001). 
</p>
<h3>Growth conditions </h3>

<p>Synechocystis sp. PCC 6803 can grow under photoautotrophic, heterotrophic and mixotrophic conditions, besides being able to develop under low CO2 concentrations (Ikeuchi & Tabata 2001). 
</p>
<p>Since it cannot fix nitrogen naturally, Synechocystis sp. PCC 6803 produces cyanophycin as a way of storing ammonium nitrogen inside of the cell.  On the other hand, its phosphorous source consists on inorganic polyphosphate inside of the cell. It is worth mentioning that both nitrogen and phosphorous supplements are necessary to cultivate this cyanobacterium in order to avoid chlorosis (Yu et al. 2013).
</p>
<p>Synechocystis sp. PCC 6803 is generally cultivated on a BG11 medium (pH 7-8) at 30ºC and its cells are maintained in an alkaline medium (pH 10) and oxic conditions. This species, under ideal conditions, can accomplish a specific growth rate of 1.7-2.5 day-1, although this rates are reduced in unfavourable environments. 
</p>
<h3>Uses and applications</h3>

<p>Besides being used as a model for photosynthetic studies, Synechocystis sp. PCC 6803 has been the host strain for the production of a series of bioproducts, including the following:</p>


<ul>
  <li>Biofuels, such as ethanol, hydrogen (H2), isobutanol and fatty acids, which are a precursor for diesel fuels. </li>
  <li>Biomaterials, such as acetone, ethylene and isoprene, the last two being precursors for synthetic rubber and polymers (Yu et al. 2013).</li>
  <li>Health-related compounds, such as fatty acids since they also act as precursors for omega-3 based pharmaceuticals (Borowitzka 2013). </li>
</ul>

