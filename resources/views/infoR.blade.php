<h1>RHODOCOCCUS JOSTII RHA1</h1>
	
<ul>
  <li>Kingdom: Bacteria </li>
  <li> Phylum: Cyanobacteria</li>
  <li>Order: Chroococcales </li>
  <li>Genus: Synechocystis </li>
</ul>

<h3>Definition and characterisation</h3>

<p>SRhodococcus sp. is a type of bacteria that lives on diverse environments and is one of the most industrially relevant genus of actinomycetes not related to antibiotics. Specifically, Rhodococcus jostii RHA1 (also known as Rhodococcus sp. RHA1) is a Gram positive, aerobic, mesophilic, non-sporulating soil actinomycete that is capable of degrading hydrophobic natural compounds and xenobiotics, such as polychlorinated biphenyls (PCBs) (McLeod et al. 2006). 
</p>
<p>Comparing this organism with other actynomycetes, Rhodococcus jostii RHA1 presents some advantages such as a faster growth than Mycobacterium tuberculosis and a simpler developmental cycle than Streptomycetes (McLeod et al. 2006).
</p>
<p>Although this genus has not been well-characterised, the genome of Rhodococcus jostii RHA1 has been sequenced (9,702,737 bp with a high G+C content) and, through 16S RNA sequencing, it is known that the strain is related to Rhodococcus opacus (McLeod et al. 2006). 
</p>
<p>Furthermore, it is known that its genome is comprised into a chromosome of unspecified topology and three linear plasmids: pRHL1 (1,100 kb), pRHL2 (450 kb) and pHRL3 (330 kb). It is worth mentioning that the byphenyl (bph) genes are located on the chromosome and the plasmids pRHL1 and pRHL2 (Warren et al. 2004)</p>
<h3>Growth conditions </h3>

<p>Rhodococcus jostii RHA1 uses an extensive assortment of aromatic compounds, nitriles, carbohydrates, steroids, among other compounds, as carbon and energy sources (McLeod et al. 2006).  
</p>
<p>It has been documented that the optimal conditions for growing this strain are at 28-30ºC using a DSMZ Medium 535 (trypticase soy broth agar) (DSMZ 2014), although it can still be grown in other medium such as LB (Luria Bertani) (Warren et al. 2004). Its doubling time has been theoretically and experimentally reported to be 3.4 ± 0.3 h (Swain et al. 2012).</p>


<h3>Uses and applications</h3>


<p>This microbial biocatalyst, which is considered safe since it is categorised in the risk group 1 (DSMZ 2014), has applications in the environmental, chemical, energy and pharmaceutical sectors. 
</p>
<p>As it was previously mentioned, Rhodococcus jostii RHA1 is applied in bioremediation practices as it has the ability to degrade PCBs, a variety of chlorinated compounds that affect animal and human health by causing skin conditions, liver damage and even cancer (Recio-Vega et al. 2011). Moreover, this strain can be applied for fossil fuel biodesulfurization (McLeod et al. 2006). Regarding other industrially important applications, Rhodococcus jostii RHA1 is able to produce acrylamide and acrylic acid in a large-scale scenario and it has the potential to be used in the process of bioactive steroid production (Van Der Geize & Dijkhuizen 2004)</p>

